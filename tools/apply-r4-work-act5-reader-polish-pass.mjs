for (const block of data.act5_detail_blocks ?? []) {
  for (const direction of block.choice_window?.directions ?? []) {
    for (const group of direction.micro_groups ?? []) {
      for (const option of group.options ?? []) {
        if (option.body?.includes('没有立刻变轻')) {
          const replacement = microBodies.get(option.label);
          if (!replacement) {
            throw new Error(`Missing micro body replacement for ${option.option_id}: ${option.label}`);
          }
          option.body = replacement;
        }
      }
    }
  }
}

const remaining = [];
walkStrings(data, (value) => {
  if (value.includes('没有立刻变轻') || value.includes('选择诱导') || value.includes('四个入口')) remaining.push(value);
  return value;
});

if (remaining.length) {
  throw new Error(`Reader polish pass left ${remaining.length} flagged strings`);
}

