function page(page_id, page_title, location, text, updates = {}) {
  return {
    page_id,
    page_title,
    location,
    text,
    updates: {
      ...updates,
      [passFlag]: true,
    },
  };
}

function updateChoice(block, title, guide, directions = {}) {
  if (!block.choice_window) return;
  if (title) block.choice_window.title = title;
  if (guide) block.choice_window.guide = guide;
  for (const direction of block.choice_window.directions || []) {
    const patch = directions[direction.direction || direction.key];
    if (!patch) continue;
    Object.assign(direction, patch);
  }
}

function findBlock(id) {
  const block = data.act5_detail_blocks.find((item) => item.block_id === id);
  if (!block) throw new Error(`Missing block ${id}`);
  return block;
}

