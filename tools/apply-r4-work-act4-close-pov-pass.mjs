function updateJson() {
  for (const block of route.act4_detail_blocks || []) {
    for (const page of block.pages || []) {
      if (pageTexts[page.page_id]) {
        page.text = playerParagraphs(pageTexts[page.page_id]);
      } else if (Array.isArray(page.text)) {
        page.text = page.text.map(playerText);
      }
      page.page_title = playerText(page.page_title);
      page.location = playerText(page.location);
    }

    if (block.choice_window) {
      const guide = choiceGuides[block.choice_window.choice_window_id];
      if (guide) block.choice_window.guide = playerText(guide);
      for (const direction of block.choice_window.directions || []) {
        if (choiceDescriptions[direction.direction_id]) {
          direction.description = playerText(choiceDescriptions[direction.direction_id]);
        } else {
          direction.description = playerText(direction.description);
        }
        for (const page of direction.feedback_pages || []) {
          if (pageTexts[page.page_id]) {
            page.text = playerParagraphs(pageTexts[page.page_id]);
          } else if (Array.isArray(page.text)) {
            page.text = page.text.map(playerText);
          }
          page.page_title = playerText(page.page_title);
          page.location = playerText(page.location);
        }
        for (const group of direction.micro_groups || []) {
          group.prompt = playerText(group.prompt);
          group.guide = playerText(group.guide);
          for (const option of group.options || []) {
            option.body = playerText(option.body);
          }
        }
      }
    }
  }
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function updateMarkdown() {
  let md = fs.readFileSync(docPath, "utf8");

  for (const [pageId, paragraphs] of Object.entries(pageTexts)) {
    const headingPattern = new RegExp("(### `" + escapeRegExp(pageId) + "`[^\\n]*\\n\\n)([\\s\\S]*?)(?=\\n### `|\\n---\\n\\n## |\\n## R4-ACT4-|$)");
    const replacement = `$1${playerParagraphs(paragraphs).join("\n\n")}\n`;
    if (headingPattern.test(md)) {
      md = md.replace(headingPattern, replacement);
    }
  }

  for (const [choiceId, guide] of Object.entries(choiceGuides)) {
    const sectionPattern = new RegExp("(### `" + escapeRegExp(choiceId) + "`[\\s\\S]*?抉择引导文：)([^\\n]*)([\\s\\S]*?)(?=\\n#### A |\\n### `|\\n---\\n\\n## |$)");
    if (sectionPattern.test(md)) {
      md = md.replace(sectionPattern, `$1${playerText(guide)}$3`);
    } else {
      const insertPattern = new RegExp("(### `" + escapeRegExp(choiceId) + "`[\\s\\S]*?位置：[^\\n]*\\n)");
      md = md.replace(insertPattern, `$1\n抉择引导文：${playerText(guide)}\n`);
    }
  }

  for (const [choiceId, rows] of Object.entries(choiceTables)) {
    const table = [
      "| 方向 | 类型 | 玩家可见标签 | 心态抉择 | 内流 / 主轴说明 | 当场动作 | 被延后的压力 | 反馈页 |",
      "|---|---|---|---|---|---|---|---|",
      ...rows.map((row) => `| ${row.map(playerText).join(" | ")} |`)
    ].join("\n");
    const tablePattern = new RegExp("(### `" + escapeRegExp(choiceId) + "`[\\s\\S]*?)(\\| 方向 \\| 类型 \\| (?:玩家可见标签|标签) \\|[\\s\\S]*?)(?=\\n\\n方向内流链样板：)");
    if (tablePattern.test(md)) {
      md = md.replace(tablePattern, `$1${table}`);
    }
  }

  md = md.replaceAll("林亦舟", "你");
  md = softenSecondPerson(md);
  md = md.replaceAll(" / 现场行动", "");
  md = md.replaceAll(" / 承接回声", "");
  fs.writeFileSync(docPath, md, "utf8");
}

