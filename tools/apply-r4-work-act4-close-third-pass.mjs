function updateJson() {
  for (const block of route.act4_detail_blocks || []) {
    for (const page of block.pages || []) {
      if (pageTexts[page.page_id]) page.text = pageTexts[page.page_id];
    }

    const window = block.choice_window;
    if (!window) continue;
    if (choiceGuides[window.choice_window_id]) window.guide = choiceGuides[window.choice_window_id];

    for (const direction of window.directions || []) {
      if (choiceDescriptions[direction.direction_id]) {
        direction.description = choiceDescriptions[direction.direction_id];
      }
      for (const page of direction.feedback_pages || []) {
        if (pageTexts[page.page_id]) page.text = pageTexts[page.page_id];
      }
    }
  }
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function updateMarkdown() {
  let md = fs.readFileSync(docPath, "utf8");

  for (const [pageId, paragraphs] of Object.entries(pageTexts)) {
    const headingPattern = new RegExp("(### `" + escapeRegExp(pageId) + "`[^\\n]*\\n\\n)([\\s\\S]*?)(?=\\n### `|\\n---\\n\\n## |\\n## R4-ACT4-|$)");
    if (headingPattern.test(md)) {
      md = md.replace(headingPattern, `$1${paragraphs.join("\n\n")}\n`);
    }
  }

  for (const [choiceId, guide] of Object.entries(choiceGuides)) {
    const guidePattern = new RegExp("(### `" + escapeRegExp(choiceId) + "`[\\s\\S]*?抉择引导文：)([^\\n]*)");
    if (guidePattern.test(md)) {
      md = md.replace(guidePattern, `$1${guide}`);
    }
  }

  for (const [directionId, description] of Object.entries(choiceDescriptions)) {
    const block = route.act4_detail_blocks.find((item) =>
      item.choice_window?.directions?.some((direction) => direction.direction_id === directionId)
    );
    const direction = block?.choice_window?.directions?.find((item) => item.direction_id === directionId);
    if (!direction) continue;
    const label = escapeRegExp(direction.label);
    const rowPattern = new RegExp(`(\\| [ABC] \\| [^\\n]*? \\| ${label} \\| [^|]*? \\| [^|]*? \\| )[^|\\n]*( \\| [^\\n]*? \\| [^\\n]*? \\|)`);
    md = md.replace(rowPattern, `$1${description}$2`);
  }

  fs.writeFileSync(docPath, md, "utf8");
}

