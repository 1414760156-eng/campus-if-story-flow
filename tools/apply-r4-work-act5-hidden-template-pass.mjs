let count = 0;

function walk(node) {
  if (typeof node === "string") return replacements.get(node) ?? node;
  if (Array.isArray(node)) {
    for (let i = 0; i < node.length; i += 1) {
      const before = node[i];
      node[i] = walk(node[i]);
      if (typeof before === "string" && before !== node[i]) count += 1;
    }
    return node;
  }
  if (node && typeof node === "object") {
    for (const key of Object.keys(node)) {
      const before = node[key];
      node[key] = walk(node[key]);
      if (typeof before === "string" && before !== node[key]) count += 1;
    }
  }
  return node;
}

